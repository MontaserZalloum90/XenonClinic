// Documentation data and types for the Enterprise Documentation Portal

export interface DocSection {
  id: string;
  title: string;
  path: string;
  icon?: string;
  children?: DocSection[];
}

export interface Module {
  id: string;
  name: string;
  category: string;
  description: string;
  route: string;
  icon: string;
  businessValue: string;
  personas: string[];
  features: Feature[];
  permissions: string[];
  journeyCount: number;
}

export interface Feature {
  id: string;
  name: string;
  description: string;
}

export interface Persona {
  id: string;
  name: string;
  category: string;
  description: string;
  responsibilities: string[];
  accessScope: {
    level: string;
    dataAccess: string;
    roleType: string;
  };
  permissions: string[];
  primaryModules: string[];
  coreJourneys: string[];
  entryPoints: string[];
  commonTasks: string[];
  tips: string[];
}

export interface JourneyStep {
  number: number;
  action: string;
  owner: string;
  systemResponse: string;
  validations: string[];
  rbac: string | null;
  screenshot?: string;
  auditLog?: string;
  notifications?: string[];
}

export interface Journey {
  id: string;
  name: string;
  module: string;
  category: string;
  priority: string;
  description: string;
  goal: string;
  personas: string[];
  preconditions: string[];
  steps: JourneyStep[];
  edgeCases: string[];
  successCriteria: string[];
  estimatedDuration: string;
  relatedJourneys: string[];
}

